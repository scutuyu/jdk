public class ResourceUtil {

    /**
     * 获取资源文件流
     * @param clazz 类对象
     * @param name 文件名
     * @return
     */
    public static final InputStream getResourceAsStream(Class clazz, String name){
        InputStream inputStream = clazz.getClassLoader().getResourceAsStream(name);
        return inputStream;
    }
}
