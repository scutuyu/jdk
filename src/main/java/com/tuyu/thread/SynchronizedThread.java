public class SynchronizedThread implements Runnable {
    private Object object;

    public SynchronizedThread(Object object) {
        this.object = object;
    }

    @Override
    public void run() {
        // 先运行2秒钟，在去获取临界资源的锁
        synchronized (object){
            long now = System.currentTimeMillis();
            while (System.currentTimeMillis() - now < 2000L){
                // do nothing
            }
        }
    }
}
